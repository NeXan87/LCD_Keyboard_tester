#pragma once

// =============================================================================
// ПИНЫ ПОДКЛЮЧЕНИЯ ДИСПЛЕЯ LCD1602 (4-битный режим)
// Используются цифровые пины Arduino Uno/Nano
// =============================================================================
#define LCD_RS_PIN 8  // Пин RS (Register Select): 0 = команда, 1 = данные
#define LCD_EN_PIN 9  // Пин EN (Enable): фронт запускает чтение данных
#define LCD_D4_PIN 4  // Младший бит шины данных (D4)
#define LCD_D5_PIN 5  // D5
#define LCD_D6_PIN 6  // D6
#define LCD_D7_PIN 7  // Старший бит шины данных (D7)

// =============================================================================
// ПАРАМЕТРЫ ДИСПЛЕЯ
// =============================================================================
#define LCD_COLS 16  // Количество символов в строке
#define LCD_ROWS 2   // Количество строк

// =============================================================================
// ПИНЫ АНАЛОГОВЫХ КНОПОК
// =============================================================================
#define BUTTONS_PIN A0  // Аналоговый вход для резистивной клавиатуры

// =============================================================================
// ПОРОГОВЫЕ ЗНАЧЕНИЯ ДЛЯ АНАЛОГОВЫХ КНОПОК (ADC, 10-битный диапазон: 0–1023)
// Значения подобраны для типовой LCD Keypad Shield.
// РЕКОМЕНДУЕТСЯ УТОЧНИТЬ ЧЕРЕЗ КАЛИБРОВКУ!
// Порядок: от минимального к максимальному напряжению (RIGHT → SELECT)
// =============================================================================
#define BUTTONS_TOTAL 5        // Количество кнопок
#define BUTTON_RIGHT_ADC 50    // Нажата кнопка "RIGHT"
#define BUTTON_UP_ADC 150      // Нажата кнопка "UP"
#define BUTTON_DOWN_ADC 300    // Нажата кнопка "DOWN"
#define BUTTON_LEFT_ADC 500    // Нажата кнопка "LEFT"
#define BUTTON_SELECT_ADC 700  // Нажата кнопка "SELECT"

// =============================================================================
// ПАРАМЕТРЫ ОБРАБОТКИ КНОПОК
// =============================================================================
#define BUTTONS_DEBOUNCE_MS 25  // Время антидребезга в миллисекундах

// =============================================================================
// СИСТЕМНЫЕ НАСТРОЙКИ
// =============================================================================
#define LOOP_DELAY_MS 10  // Задержка в основном цикле (для снижения нагрузки)

// =============================================================================
// ИДЕНТИФИКАТОРЫ ЛОГИЧЕСКИХ ЭКРАНОВ (SCREENS)
// Используются для переключения между режимами отображения
// =============================================================================
typedef enum {
    SCREEN_MAIN,         ///< Главный экран
    SCREEN_SETTINGS,     ///< Меню настроек
    SCREEN_DIAGNOSTICS,  ///< Диагностическая информация
    SCREEN_ABOUT,        ///< Информация о системе
    SCREEN_COUNT         ///< Количество экранов (не использовать как экран)
} ScreenId;

// Идентификаторы кнопок (enum для читаемости)
enum ButtonId {
    BUTTON_RIGHT,
    BUTTON_UP,
    BUTTON_DOWN,
    BUTTON_LEFT,
    BUTTON_SELECT
};
